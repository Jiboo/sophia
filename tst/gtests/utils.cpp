#include <gtest/gtest.h>

#include "utils.hpp"

using namespace sophia;

TEST(Utils, ParseHex) {
  u256 lTest;
  u256 lRef{{{0x36, 0xda, 0xe1, 0x1f, 0xe2, 0x19, 0x9e, 0x21, 0xb9, 0x78, 0x99, 0xaa, 0x41, 0x4d, 0x7c, 0xd7,
              0xe2, 0xd8, 0x73, 0x41, 0xf3, 0x81, 0xc5, 0xaa, 0x86, 0xd1, 0x02, 0x83, 0x72, 0x2e, 0x60, 0x04}}};
  parseHex(lTest.view(), "36dae11fe2199e21b97899aa414d7cd7e2d87341f381c5aa86d10283722e6004");
  EXPECT_EQ(lTest, lRef);
}
